class Colormap {
    constructor(colors) {
        this.colors = colors
    }

    get_color(x) {
        if (x > 1.0) {
            x = 1.0;
        } else if (x < 0.0) {
            x = 0.0;
        }

        var n = this.colors.length

        var ii = Math.round(x * (n - 1))
        var color = this.colors[ii]

        return color
    }

    get_colors() {
    	return this.colors;
    }
};

